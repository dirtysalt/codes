from typing import List


class Solution:
    def sampleStats(self, count: List[int]) -> List[float]:
        n = len(count)
        _max = max(count)

        ans = []
        # min, max
        for i in range(n):
            if count[i] != 0:
                ans.append(i)
                break
        for i in reversed(range(n)):
            if count[i] != 0:
                ans.append(i)
                break

        # avg
        ss = 0
        s = 0
        for i in range(n):
            ss += count[i] * i
            s += count[i]
        ans.append(ss / s)

        # median
        exp_rank = [(s + 1) // 2]
        if s % 2 == 0:
            exp_rank.append(exp_rank[-1] + 1)
            # print(exp_rank)

        p = 1
        st = []
        for i in range(n):
            # ranked as [x, y]
            x, y = p, p + count[i] - 1
            p += count[i]
            if x > exp_rank[-1]:
                break

            ok = False
            for exp in exp_rank:
                if x <= exp <= y:
                    ok = True
                    break
            if ok:
                st.append(i)
        med = sum(st) / len(st)
        ans.append(med)

        # most
        for i in range(n):
            if count[i] == _max:
                ans.append(i)
                break

        # ok.
        ans = [round(x, 5) for x in ans]
        return ans


