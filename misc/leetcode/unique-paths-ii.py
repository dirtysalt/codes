class Solution(object):
    def uniquePathsWithObstacles(self, obstacleGrid):
        """
        :type obstacleGrid: List[List[int]]
        :rtype: int
        """
        mat = obstacleGrid
        n = len(mat)
        if n == 0: return 0
        m = len(mat[0])
        if m == 0: return 0

        dp = [[0] * m for _ in range(2)]
        now = 0
        dp[now][0] = 1
        for i in range(n):
            for j in range(m):
                if mat[i][j] == 1:
                    dp[1 - now][j] = 0
                else:
                    dp[1 - now][j] = dp[now][j] + (dp[1 - now][j - 1] if j > 0 else 0)
            now = 1 - now
        return dp[now][m - 1]


