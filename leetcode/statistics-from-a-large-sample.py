from typing import List


class Solution:
    def sampleStats(self, count: List[int]) -> List[float]:
        ans = []
        arr = [(idx, value) for (idx, value) in enumerate(count) if value != 0]
        ans.append(arr[0][0])
        ans.append(arr[-1][0])

        tt = sum((idx * value) for (idx, value) in arr)
        cnt = sum(value for (idx, value) in arr)
        ans.append(tt / cnt)

        # median is little anonying
        if cnt % 2 == 1:
            a = b = (cnt + 1) // 2
        else:
            a = cnt // 2
            b = a + 1

        print(a, b)
        res = 0
        tmp = []
        for (idx, value) in arr:
            c, d = res + 1, res + value
            # print(c, d, idx)
            res += value

            if c > b:
                break
            if d < a:
                continue
            # overlapped
            tmp.append(idx)
        if len(tmp) == 1:
            ans.append(tmp[0])
        else:
            ans.append((tmp[0] + tmp[1]) / 2)

        # mode.
        mode_idx = 0
        mode_value = 0
        for (idx, value) in arr:
            if value > mode_value:
                mode_idx = idx
                mode_value = value
        ans.append(float(mode_idx))

        ans = [round(x * 1.0, 5) for x in ans]
        return ans


